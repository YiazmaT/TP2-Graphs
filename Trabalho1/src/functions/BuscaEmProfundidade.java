    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       Grafo grafo;
       if(pai.isMatrizSelected()) grafo = Main.matriz;
       else grafo = Main.lista;
       
       if(grafo == null){
           JOptionPane.showMessageDialog(null, "Nenhum grafo foi carregado!");
           return;
       }
       
       
       int raizBusca = Integer.parseInt(jTextField1.getText());
       if(raizBusca >= grafo.getNumVertices() || raizBusca < 0){
           JOptionPane.showMessageDialog(null, "O vertice inicial deve ser menor que: " + grafo.getNumVertices());
           return;
       }
       
       BuscaProfundidade busca = new BuscaProfundidade(grafo);
       busca.buscaProfundidade(raizBusca);
       
       atualizarTabela(grafo, busca);
    }//GEN-LAST:event_jButton2ActionPerformed

    public void atualizarTabela(Grafo grafo, BuscaProfundidade busca)
    {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        String[] tabela = new String[3];
        
        while(dtm.getRowCount() > 0)
        {
            dtm.removeRow(0);
        }
        
        for(int i = 0; i < grafo.getNumVertices(); i++)
        {
            tabela[0] = String.valueOf(i);
            tabela[1] = String.valueOf(busca.getTempoChegada(i));
            tabela[2] = String.valueOf(busca.getTempoFinaliza(i));
            dtm.addRow(tabela);
        }
    }

