    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Grafo grafo;
       if(pai.isMatrizSelected()) grafo = Main.matriz;
       else grafo = Main.lista;
       
       int verticeU,verticeV;
       
       if(grafo == null){
           JOptionPane.showMessageDialog(null, "Nenhum grafo foi carregado!");
           return;
       }
       
       
       verticeU = Integer.parseInt(VerticeU.getText());
       verticeV = Integer.parseInt(jTextField1.getText());
       if(verticeU >= grafo.getNumVertices() || verticeV >= grafo.getNumVertices()
               || verticeU < 0 || verticeV < 0){
           JOptionPane.showMessageDialog(null, "O vertice deve ser menor que: " + grafo.getNumVertices());
           return;
       }
       
       
       if(jRadioButton1.isSelected())
       {
           BuscaLargura busca = new BuscaLargura(grafo);
           busca.buscaLargura(verticeU);
           jTextArea1.setText(busca.getCaminho(verticeV));
       }
       else
       {
           BuscaProfundidade busca = new BuscaProfundidade(grafo);
           busca.buscaProfundidade(verticeU);
           jTextArea1.setText(busca.getCaminho(verticeV));
       }
    }//GEN-LAST:event_jButton1ActionPerformed


